def Add(num1,num2):
	result=num1+num2
	return result

def Sub(num1,num2):
	if(num1>num2):
		result=num1-num2
	else:
		result=num2-num1
	return result

def Mul(num1,num2):
	result=num1*num2
	return result

def Div(num1,num2):
	if(num1>num2):
		result=num1/num2
	else:
		result=num2/num1
	return result


